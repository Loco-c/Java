
public class operations {

	public int sub(int num1, int num2) {

		int sub = num1 - num2;
		return sub;
	}

	public int add(int num1, int num2) {

		int add = num1 + num2;
		return add;
	}

	public int multi(int num1, int num2) {

		int multi = num1 * num2;
		return multi;
		
		
	}

	public int div(int num1, int num2) {

		int div = num1 / num2;
		return div;
	}

}
